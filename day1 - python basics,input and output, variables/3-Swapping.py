# swapping mechanisms

# 1 using temp variable
a = 10
b = 20
print(f"a = {a} and b = {b}")

# Swapping using temp
temp = a
a = b
b = temp
print(f"a = {a} and b = {b}")


# 2 Swapping in python
x = 10
y = 20
print(f"\nx = {x} and y = {y}")

# special swapping mechanism in python
[x,y] = [y,x]
print(f"x = {x} and y = {y}")

